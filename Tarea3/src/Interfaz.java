    public class Hilo extends Thread {
        private String abcdario = txtLetra.getText().toLowerCase();
        @Override
        public void run() {
            String[] letras = {"a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "Ã±", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z"};
            for (int i = 0; i < letras.length; i++) {
                if (abcdario.equals(letras[i])) {
                    System.out.println("nu: " + i);
                    pos = i;
                    ready = true;
                }
            }
            if (ready == true) {
                for (int i = pos + 1; i < letras.length; i++) {
                    System.out.print(letras[i] + " ");
                    try {
                        lblletra.setText(letras[i]);
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }
    
    private void txtNumero(java.awt.event.KeyEvent evt) {
        char car = evt.getKeyChar();
        if (Character.isDigit(car)) {
        } else {
            evt.consume();
            getToolkit().beep();
        }
        if (txtNumero.getText().length() == 3) {
            evt.consume();
        }
    } 
    
    private void txtLetra(java.awt.event.KeyEvent evt) {
        char car = evt.getKeyChar();
        if (Character.isLetter(car)) {
        } else {
            evt.consume();
            getToolkit().beep();
        }
        if (txtLetra.getText().length() == 1) {
            evt.consume();
        }
    }
    
